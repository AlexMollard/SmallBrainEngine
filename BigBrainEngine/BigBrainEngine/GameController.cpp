#include "GameController.h"

unsigned int depthMapFBO;
unsigned int depthCubemap;
const unsigned int SHADOW_WIDTH = 1024, SHADOW_HEIGHT = 1024;
glm::vec3 lightPos(0.0f, 0.5f, 0.0f);
void renderCube();
void renderScene(const Shader &shader);
// settings
const unsigned int SCR_WIDTH = 1920;
const unsigned int SCR_HEIGHT = 1080;
GameController::GameController(Camera* camera)
{
	this->camera = camera;
	textRenderer = new TextRenderer();
	graphicManager = new GraphicsManager();
	gameObjectManager = new GameObjectManager(graphicManager);

	lightingShader = new Shader("PointShadow_vert.shader", "PointShadow_frag.shader");
	lightShader = new Shader("PointShadowDepth_vert.shader", "PointShadowDepth_frag.shader", "PointShadowDepth_geo.shader");

	// TextShader
	textShader = new Shader("TextShader_vert.shader", "TextShader_frag.shader");

	graphicManager->AddShader(lightingShader);
	graphicManager->AddShader(lightShader);
	graphicManager->AddShader(textShader);

	texture = new Texture("ImageTestOne.bmp");
	texture2 = new Texture("ImageTestTwoB.bmp");

	planeMat = new Material(lightingShader, texture2);
	lightMat = new Material(lightShader, texture);
	textMat = new Material(textShader);
	graphicManager->AddMaterial(planeMat);
	graphicManager->AddMaterial(lightMat);

	hexModel = new Model("../BigBrainEngine/Models/Hex.fbx");
	plane = new Model("../BigBrainEngine/Models/Plane.obj");
	graphicManager->AddModel(hexModel);
	graphicManager->AddModel(plane);

	for (int i = 0; i < 10; i++)
	{
		tempObjects.push_back(new GameObject(plane, planeMat));
		//tempObjects.back()->SetScale(glm::vec3(0.005f,0.005f,0.005f));
		tempObjects.back()->SetPostition(glm::vec3(i * 2,0,0));
		gameObjectManager->AddObject(tempObjects.back());
	}

	// FPS setup
	previousTime = glfwGetTime();
	frameCount = 0;


	depthMapFBO = 0;
	depthCubemap = 0;
	glGenFramebuffers(1, &depthMapFBO);
	// create depth cubemap texture
	glGenTextures(1, &depthCubemap);
	glBindTexture(GL_TEXTURE_CUBE_MAP, depthCubemap);
	for (unsigned int i = 0; i < 6; ++i)
		glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_DEPTH_COMPONENT, SHADOW_WIDTH, SHADOW_HEIGHT, 0, GL_DEPTH_COMPONENT, GL_FLOAT, NULL);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);
	// attach depth texture as FBO's depth buffer
	glBindFramebuffer(GL_FRAMEBUFFER, depthMapFBO);
	glFramebufferTexture(GL_FRAMEBUFFER, GL_DEPTH_ATTACHMENT, depthCubemap, 0);
	glDrawBuffer(GL_NONE);
	glReadBuffer(GL_NONE);
	glBindFramebuffer(GL_FRAMEBUFFER, 0);

	// shader configuration
	// --------------------
	lightingShader->use();
	lightingShader->setInt("diffuseTexture", 0);
	lightingShader->setInt("depthMap", 1);
	lightingShader->setInt("shadows", 1);

}


GameController::~GameController()
{
	for (int i = 0; i < tempObjects.size(); i++)
	{
		delete tempObjects[i];
	}

	delete lightingShader;
	delete lightShader;
	delete textShader;
	delete texture;
	delete texture2;
	delete planeMat;
	delete lightMat;
	delete textMat;
	delete hexModel;
	delete plane;
	delete graphicManager;
	delete gameObjectManager;
	delete textRenderer;
}

void GameController::Update(float deltaTime, glm::mat4* projection, glm::mat4* view)
{
	GetFPS();
	Draw(projection, view);
}


void GameController::Draw(glm::mat4* projection, glm::mat4* view)
{
	// move light position over time
	lightPos.z = sin(glfwGetTime() * 0.5) * 3.0;

	// 0. create depth cubemap transformation matrices
	// -----------------------------------------------
	float near_plane = 1.0f;
	float far_plane = 25.0f;
	glm::mat4 shadowProj = glm::perspective(glm::radians(90.0f), (float)SCR_WIDTH / (float)SCR_HEIGHT, near_plane, far_plane);
	std::vector<glm::mat4> shadowTransforms;
	shadowTransforms.push_back(shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(1.0f, 0.0f, 0.0f), glm::vec3(0.0f, -1.0f, 0.0f)));
	shadowTransforms.push_back(shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(-1.0f, 0.0f, 0.0f), glm::vec3(0.0f, -1.0f, 0.0f)));
	shadowTransforms.push_back(shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(0.0f, 1.0f, 0.0f), glm::vec3(0.0f, 0.0f, 1.0f)));
	shadowTransforms.push_back(shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(0.0f, -1.0f, 0.0f), glm::vec3(0.0f, 0.0f, -1.0f)));
	shadowTransforms.push_back(shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(0.0f, 0.0f, 1.0f), glm::vec3(0.0f, -1.0f, 0.0f)));
	shadowTransforms.push_back(shadowProj * glm::lookAt(lightPos, lightPos + glm::vec3(0.0f, 0.0f, -1.0f), glm::vec3(0.0f, -1.0f, 0.0f)));
	
	// 1. render scene to depth cubemap
	// --------------------------------
	glViewport(0, 0, SHADOW_WIDTH, SHADOW_HEIGHT);
	glBindFramebuffer(GL_FRAMEBUFFER, depthMapFBO);
	glClear(GL_DEPTH_BUFFER_BIT);
	lightShader->use();
	for (unsigned int i = 0; i < 6; ++i)
		lightShader->setMat4("shadowMatrices[" + std::to_string(i) + "]", shadowTransforms[i]);

	lightShader->setFloat("far_plane", far_plane);
	lightShader->setVec3("lightPos", lightPos);
	renderScene(*lightShader);
	glBindFramebuffer(GL_FRAMEBUFFER, 0);

	// 2. render scene as normal 
	// -------------------------
	glViewport(0, 0, SCR_WIDTH, SCR_HEIGHT);
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
	lightingShader->use();
	lightingShader->setMat4("projection", *projection);
	lightingShader->setMat4("view", *view);

	// set lighting uniforms
	lightingShader->setVec3("lightPos", lightPos);
	lightingShader->setVec3("viewPos", camera->Position);
	lightingShader->setFloat("far_plane", far_plane);
	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_2D, texture->imageID);
	glActiveTexture(GL_TEXTURE1);
	glBindTexture(GL_TEXTURE_CUBE_MAP, depthCubemap);
	renderScene(*lightingShader);


	//gameObjectManager->Draw(projection, view);

	// Displaying FPS
	std::string fps = "FPS: ";
	fps += to_string(FPS);
	graphicManager->SetMaterial(textMat);
	textRenderer->RenderText(textShader, fps, 20.0f, 30.0f, 1.0f, glm::vec3(0.5, 0.8f, 0.2f));
}

void GameController::GetFPS()
{
	double currentTime = glfwGetTime();
	frameCount++;
	// If a second has passed.
	if (currentTime - previousTime >= 1.0)
	{
		FPS = frameCount;

		frameCount = 0;
		previousTime = currentTime;
	}
}

// renders the 3D scene
// --------------------
void renderScene(const Shader &shader)
{
	// room cube
	glm::mat4 model = glm::mat4(1.0f);
	model = glm::scale(model, glm::vec3(5.0f));
	shader.setMat4("model", model);
	glDisable(GL_CULL_FACE); // note that we disable culling here since we render 'inside' the cube instead of the usual 'outside' which throws off the normal culling methods.
	shader.setInt("reverse_normals", 1); // A small little hack to invert normals when drawing cube from the inside so lighting still works.
	renderCube();
	shader.setInt("reverse_normals", 0); // and of course disable it
	glEnable(GL_CULL_FACE);
	// cubes
	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(4.0f, -3.5f, 0.0));
	model = glm::scale(model, glm::vec3(0.5f));
	shader.setMat4("model", model);
	renderCube();
	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(2.0f, 3.0f, 1.0));
	model = glm::scale(model, glm::vec3(0.75f));
	shader.setMat4("model", model);
	renderCube();
	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(-3.0f, -1.0f, 0.0));
	model = glm::scale(model, glm::vec3(0.5f));
	shader.setMat4("model", model);
	renderCube();
	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(-1.5f, 1.0f, 1.5));
	model = glm::scale(model, glm::vec3(0.5f));
	shader.setMat4("model", model);
	renderCube();
	model = glm::mat4(1.0f);
	model = glm::translate(model, glm::vec3(-1.5f, 2.0f, -3.0));
	model = glm::rotate(model, glm::radians(60.0f), glm::normalize(glm::vec3(1.0, 0.0, 1.0)));
	model = glm::scale(model, glm::vec3(0.75f));
	shader.setMat4("model", model);
	renderCube();
}

